export type Language = 'en' | 'fr' | 'ar';

export const formatCurrency = (amount: number, lang: Language) => {
  const locale = lang === 'ar' ? 'ar-MA' : lang === 'fr' ? 'fr-MA' : 'en-MA';
  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency: 'MAD'
  }).format(amount);
};

